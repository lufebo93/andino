    public void cargarTabla() throws SQLException, IOException{
        Conexion con = new Conexion();
        con.conectar();
        ResultSet rs = null;
        
        modelo = new DefaultTableModel();
        modelo.addColumn("C.Agenda");
        modelo.addColumn("C.Usuario");
        modelo.addColumn("Usuario");
        modelo.addColumn("C.Paciente");
        modelo.addColumn("Paciente");
        modelo.addColumn("C.Empleado");
        modelo.addColumn("Empleado");
        modelo.addColumn("Fecha");
        modelo.addColumn("V.Consulta");
        modelo.addColumn("Estado");
        modelo.addColumn("Hora");
        
        try{
            rs = con.ejecutarQuery("SELECT \n" +
"agenda.\"idagenda\", \n" +
"agenda.\"idusuario\", \n" +
"usuario.\"usuario\",\n" +
"agenda.\"idpaciente\", \n" +
"paciente.\"apellido\",\n" +
"agenda.\"idempleado\",\n" +
"empleado.\"apellido\",\n" +
"agenda.\"fecha\", \n" +
"agenda.\"valorconsulta\", \n" +
"agenda.\"estado\", \n" +
"agenda.\"hora\"\n" +
"FROM public.\"agenda\" agenda INNER JOIN public.\"usuario\" usuario ON\n" +
"agenda.\"idusuario\"=usuario.\"idusuario\" INNER JOIN public.\"paciente\" paciente ON\n" +
"agenda.\"idpaciente\"=paciente.\"idpaciente\" INNER JOIN public.\"empleado\" empleado ON\n" +
"agenda.\"idempleado\"=empleado.\"idempleado\";");
                ResultSetMetaData md = rs.getMetaData();
                int columnCount = md.getColumnCount();
                String[] cols = new String[columnCount];
                for (int i=1;i<= columnCount;i++){
                    cols[i-1] = md.getColumnName(i);
                }
                while (rs.next()){
                    Object[] row = new Object[columnCount];
                    for (int i = 1 ; i <= columnCount ; i++){
                        row[i-1] = rs.getObject(i);
                    }
                    modelo.addRow(row);
                }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Error "+e,
                    "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
        }finally {
            con.desconectar();
            rs.close();
        }
        tablaAgenda.setModel(modelo);
        modelo.fireTableDataChanged();
        
        
        tablaAgenda.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaAgenda.getColumnModel().getColumn(0).setMinWidth(0);
        tablaAgenda.getColumnModel().getColumn(0).setPreferredWidth(0);
        
        tablaAgenda.getColumnModel().getColumn(1).setMaxWidth(0);
        tablaAgenda.getColumnModel().getColumn(1).setMinWidth(0);
        tablaAgenda.getColumnModel().getColumn(1).setPreferredWidth(0);
        
        tablaAgenda.getColumnModel().getColumn(3).setMaxWidth(0);
        tablaAgenda.getColumnModel().getColumn(3).setMinWidth(0);
        tablaAgenda.getColumnModel().getColumn(3).setPreferredWidth(0);
        
        tablaAgenda.getColumnModel().getColumn(5).setMaxWidth(0);
        tablaAgenda.getColumnModel().getColumn(5).setMinWidth(0);
        tablaAgenda.getColumnModel().getColumn(5).setPreferredWidth(0);
    }
    
    public void filtro() {
        int columnaABuscar = 0;
        if (cmbListarPac.getSelectedItem().equals("C.Agenda")) {
            columnaABuscar = 0;
        }
        if (cmbListarPac.getSelectedItem() == "ApellidoPaciente") {
            columnaABuscar = 1;
        }
        if (cmbListarPac.getSelectedItem() == "ApellidoMÃ©dico") {
            columnaABuscar = 2;
        }
        String replace = txtBusquedaAgenda.getText();
        replace = replace.replace("e", "(Ã©)");
        trsFiltro.setRowFilter(RowFilter.regexFilter("(?i).*"+replace+".*", columnaABuscar));
        tablaAgenda.setRowSorter(trsFiltro);
    }
    
    public void producto() throws SQLException, IOException{
        Conexion con= new Conexion();
        PreparedStatement pst = null;
        ResultSet rs = null;
        String sql = "SELECT concat(idproducto,' ',nombre)as producto\n" +
"	FROM public.producto order by idproducto asc";
        try {
           pst=con.conectar().prepareStatement(sql);
           rs = pst.executeQuery();
           cmbProductos.addItem("Seleccione");
           while(rs.next()){
               cmbProductos.addItem(rs.getString("producto"));
           }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al obtener los datos",
                    "CEBYCAM-CES", JOptionPane.ERROR_MESSAGE);
        }finally{
            con.desconectar();
            rs.close();
        }
    }
       
