    public void cargarTabla() throws SQLException, IOException{
        Conexion con = new Conexion();
        con.conectar();
        ResultSet rs = null;
        
        modelo = new DefaultTableModel();
        modelo.addColumn("H.Clinica");
        modelo.addColumn("Paciente");
        modelo.addColumn("Cedula");
        modelo.addColumn("Telefono");
        modelo.addColumn("Edad");
        modelo.addColumn("Sexo");
        modelo.addColumn("Dirección");
        modelo.addColumn("Fecha de Nacimiento");
        modelo.addColumn("Mail");
        modelo.addColumn("Estatura");
        modelo.addColumn("Peso");
        modelo.addColumn("Ocupación");
        modelo.addColumn("Foto");
        modelo.addColumn("Categoria");
        modelo.addColumn("Indicaciones");
        modelo.addColumn("Fecha");
        modelo.addColumn("hora");
        modelo.addColumn("Prescripcion");
        modelo.addColumn("Diagnostico");
        modelo.addColumn("ID Medico");
        modelo.addColumn("Medico");
        modelo.addColumn("ID Atencion");
        
        try{
            rs = con.ejecutarQuery("SELECT \n" +
"\"historia_clinica\".codigo, \n" +
"\"historia_clinica\".paciente, \n" +
"\"historia_clinica\".cedula, \n" +
"\"historia_clinica\".telefono, \n" +
"\"historia_clinica\".edad, \n" +
"\"historia_clinica\".sexo, \n" +
"\"historia_clinica\".direccion, \n" +
"\"historia_clinica\".\"fecha de nacimiento\", \n" +
"\"historia_clinica\".mail, \n" +
"\"historia_clinica\".estatura, \n" +
"\"historia_clinica\".peso, \n" +
"\"historia_clinica\".ocupacion, \n" +
"\"historia_clinica\".foto, \n" +
"\"historia_clinica\".categoria,\n" +
"\"atencion\".indicaciones, \n" +
"\"atencion\".fecha,\n" +
"\"atencion\".hora,\n" +
"\"atencion\".prescripcion, \n" +
"\"atencion\".diagnostico,\n" +
"\"medico\".codigo,\n" +
"\"medico\".nombre,\n" +
"\"atencion\".codigo as \"ID Atencion\"\n" +
"FROM public.\"historia clinica\" historia_clinica inner join public.\"atencion\" atencion on historia_clinica.codigo=atencion.\"historia clinica\"\n" +
"inner join public.\"medico\" medico on atencion.\"medico\"=medico.\"codigo\" where \"medico\".codigo="+Integer.parseInt(txtCodigoMedico.getText()));
                ResultSetMetaData md = rs.getMetaData();
                int columnCount = md.getColumnCount();
                String[] cols = new String[columnCount];
                for (int i=1;i<= columnCount;i++){
                    cols[i-1] = md.getColumnName(i);
                }
                while (rs.next()){
                    Object[] row = new Object[columnCount];
                    for (int i = 1 ; i <= columnCount ; i++){
                        row[i-1] = rs.getObject(i);
                    }
                    modelo.addRow(row);
                }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Error "+e,
                    "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
        }finally {
            con.desconectar();
            rs.close();
        }
        tabla.setModel(modelo);
        modelo.fireTableDataChanged();
        
        tabla.getColumnModel().getColumn(12).setMaxWidth(0);
        tabla.getColumnModel().getColumn(12).setMinWidth(0);
        tabla.getColumnModel().getColumn(12).setPreferredWidth(0);
        
        tabla.getColumnModel().getColumn(13).setMaxWidth(0);
        tabla.getColumnModel().getColumn(13).setMinWidth(0);
        tabla.getColumnModel().getColumn(13).setPreferredWidth(0);
        
        tabla.getColumnModel().getColumn(21).setMaxWidth(0);
        tabla.getColumnModel().getColumn(21).setMinWidth(0);
        tabla.getColumnModel().getColumn(21).setPreferredWidth(0);
    }
       
    
