    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Conexion con= new Conexion();
        try {
            con.conectar();
        } catch (IOException ex) {
            Logger.getLogger(frmModificarAtencion.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (JOptionPane.showConfirmDialog(null, "¿Esta usted seguro?", "CEBYCAM-CES",
            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            if(jtxtIndicaciones.getText().equals("")||txtCodigoPac.getText().equals("")||txtCodigoMedico.getText().equals("")
                    ||jtxtPrescripciones.getText().equals("")||txtDiagnosticoAtencion.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Debe ingresar toda la información",
                    "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
            }else{
                try{
                    String sql="UPDATE public.atencion\n" +
"   SET indicaciones=?, prescripcion=?, diagnostico=?\n" +
" WHERE codigo=?;";
                    try (PreparedStatement ps = con.conectar().prepareStatement(sql)) {
                        ps.setString(1, jtxtIndicaciones.getText());
                        ps.setString(2, jtxtPrescripciones.getText());
                        ps.setString(3, txtDiagnosticoAtencion.getText());
                        ps.setInt(4, Integer.parseInt(txtCodigoAtencion.getText()));
                        ps.execute();
                        ps.close();
                    } catch (IOException ex) {
                        Logger.getLogger(frmModificarAtencion.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JOptionPane.showMessageDialog(this, "Datos guardados correctamente...",
                        "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
                    jtxtIndicaciones.setText("");
                    jtxtPrescripciones.setText("");
                    txtDiagnosticoAtencion.setText("");
                    txtCodigoAtencion.setText("");

                    jtxtIndicaciones.setEnabled(false);
                    jtxtPrescripciones.setEnabled(false);
                    txtDiagnosticoAtencion.setEnabled(false);
                }catch(SQLException | NumberFormatException | HeadlessException x){
                    JOptionPane.showMessageDialog(rootPane, "Debe ingresar datos "+x);
                }finally {
                    con.desconectar();
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

