    public void cargarTabla() throws IOException{
        Conexion con = new Conexion();
        con.conectar();
        
        modelo = new DefaultTableModel();
        modelo.addColumn("H.Clinica");
        modelo.addColumn("Paciente");
        modelo.addColumn("Médico");
        modelo.addColumn("Especialidad");
        modelo.addColumn("Fecha");
        modelo.addColumn("Indicaciones");
        modelo.addColumn("Prescripciones");
        modelo.addColumn("Diagnostico");
        modelo.addColumn("Categoria");
        
        
        List<AtencionPrevia> lst = new ArrayList<>();
        AtencionPreviaImp dao = new AtencionPreviaImp();
        
        try {            
            lst = dao.obtener();            
        }catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, "Error al procesar los datos "+e, "CEBYCAM-CES",
                    JOptionPane.INFORMATION_MESSAGE);
        }finally {
            con.desconectar();
        }
        
        for(AtencionPrevia exp:lst){
           modelo.addRow(new Object[]{
               exp.getCodigo(), 
               exp.getPaciente(),
               exp.getMedico(),
               exp.getEspecialidad(),
               exp.getFecha(),
               exp.getIndicaciones(),
               exp.getPrescripcion(),
               exp.getDiagnostico(),
               exp.getCategoria(),
           });        
        }
        tabla.setModel(modelo);
        modelo.fireTableDataChanged();
        
        tabla.getColumnModel().getColumn(8).setMaxWidth(0);
        tabla.getColumnModel().getColumn(8).setMinWidth(0);
        tabla.getColumnModel().getColumn(8).setPreferredWidth(0);
    }
    
    public void filtro() {
        int columnaABuscar = 0;
        if (cmbListar.getSelectedItem().equals("H.Clinica")) {
            columnaABuscar = 0;
        }
        if (cmbListar.getSelectedItem() == "Paciente") {
            columnaABuscar = 1;
        }
        if (cmbListar.getSelectedItem() == "Médico") {
            columnaABuscar = 2;
        }
        String replace = txtBusqueda.getText();
        replace = replace.replace("e", "(é)");
        trsFiltro.setRowFilter(RowFilter.regexFilter("(?i).*"+replace+".*", columnaABuscar));
        tabla.setRowSorter(trsFiltro);
    }
    
    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int selectedRowIndex = tabla.rowAtPoint(evt.getPoint());
        txtCodigo.setText(tabla.getValueAt(selectedRowIndex, 0).toString());
        txtPaciente.setText(tabla.getValueAt(selectedRowIndex, 1).toString());
        txtMedicoAtenPrevia.setText(tabla.getValueAt(selectedRowIndex, 2).toString());
        txtMedicoEspecialidad.setText(tabla.getValueAt(selectedRowIndex, 3).toString());
        txtFecha.setText(tabla.getValueAt(selectedRowIndex, 4).toString());
        jtxtIndicaciones.setText(tabla.getValueAt(selectedRowIndex, 5).toString());
        jtxtPrescripciones.setText(tabla.getValueAt(selectedRowIndex, 6).toString());
        txtDiagnosticoPrevio.setText(tabla.getValueAt(selectedRowIndex, 7).toString());
        txtCategoria.setText(tabla.getValueAt(selectedRowIndex, 8).toString());
    }//GEN-LAST:event_tablaMouseClicked

