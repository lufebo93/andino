    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        Conexion con = new Conexion();
        try {
            con.conectar();
        } catch (IOException ex) {
            Logger.getLogger(frmModificarPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
        modelo.setRowCount( 0 );
        
        java.util.List<HistoriaClinica> lst = new ArrayList<>();
        HistoriaClinicaImp dao = new HistoriaClinicaImp();
        
        try {            
            lst = dao.obtener();            
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al procesar los datos "+e,
                        "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
        }finally {
            con.desconectar();
        }
        
        for(HistoriaClinica exp:lst){
           modelo.addRow(new Object[]{
               exp.getCodigo(), 
               exp.getPaciente(),
               exp.getCedula(),
               exp.getTelefono(),
               exp.getEdad(),
               exp.getSexo(),
               exp.getDireccion(),
               exp.getFecha_nacimiento(),
               exp.getMail(),
               exp.getEstatura(),
               exp.getPeso(),
               exp.getOcupacion(),
               exp.getImagen(),
           });        
        }
        tabla.setModel(modelo);
        modelo.fireTableDataChanged();
    }//GEN-LAST:event_btnRefreshActionPerformed

