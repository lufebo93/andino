    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Conexion con= new Conexion();
        try {
            con.conectar();
        } catch (IOException ex) {
            Logger.getLogger(frmModificarPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (JOptionPane.showConfirmDialog(null, "¿Esta usted seguro?", "CEBYCAM-CES",
            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            if(radiobtnImangen.isSelected()==false&&radiobtnDatos.isSelected()==false){
                JOptionPane.showMessageDialog(this, "No ha seleccionado una opción",
                    "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
            }else{
                if(radiobtnImangen.isSelected()){
                    if("camara".equals(botones)){
                        try{
                            String sql="UPDATE public.\"historia clinica\"\n" +
                            "   SET foto=? WHERE codigo="+Integer.parseInt(txtCodigo.getText());

                            try (PreparedStatement ps = con.conectar().prepareStatement(sql)) {
                                ps.setBinaryStream(1, frmCamaraModificarPac.fin, frmCamaraModificarPac.longitudBytes);
                                ps.execute();
                                ps.close();
                            } catch (IOException ex) {
                                Logger.getLogger(frmModificarPaciente.class.getName()).log(Level.SEVERE, null, ex);
                            }
                            JOptionPane.showMessageDialog(this, "Datos guardados correctamente...",
                                "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
                        }catch(SQLException | NumberFormatException | HeadlessException x){
                                JOptionPane.showMessageDialog(rootPane, "Hubo un problema al guardar la información "+x);
                        }finally {
                            con.desconectar();
                        }
                    }else{
                        if("subir".equals(botones)){
                            try{
                                String sql="UPDATE public.\"historia clinica\"\n" +
                                "   SET foto=? WHERE codigo="+Integer.parseInt(txtCodigo.getText());

                                try (PreparedStatement ps = con.conectar().prepareStatement(sql)) {
                                    ps.setBinaryStream(1, fis, longitudBytes);
                                    ps.execute();
                                    ps.close();
                                } catch (IOException ex) {
                                    Logger.getLogger(frmModificarPaciente.class.getName()).log(Level.SEVERE, null, ex);
                                }
                                JOptionPane.showMessageDialog(this, "Datos guardados correctamente...",
                                    "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
                            }catch(SQLException | NumberFormatException | HeadlessException x){
                                        JOptionPane.showMessageDialog(rootPane, "Hubo un problema al guardar la información "+x);
                            }finally {
                                con.desconectar();
                            }
                        }else{
                            if(btnCamara.isSelected()==false&&btnSubirFoto.isSelected()==false){
                                JOptionPane.showMessageDialog(this, "Debe escoger o tomar una foto",
                            "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
                            }
                        }
                    }
                }
                if(radiobtnDatos.isSelected()){
                    try{
                        String sql="UPDATE public.\"historia clinica\"\n" +
                        "   SET paciente=?, cedula=?, telefono=?, edad=?, \n" +
                        "       sexo=?, direccion=?, \"fecha de nacimiento\"=?, mail=?, estatura=?, \n" +
                        "       peso=?, ocupacion=?, enfermero=? WHERE codigo="+Integer.parseInt(txtCodigo.getText());

                        try (PreparedStatement ps = con.conectar().prepareStatement(sql)) {
                            ps.setString(1, txtPaciente.getText());
                            ps.setString(2, txtCedula.getText());
                            ps.setString(3, txtTelefono.getText());
                            ps.setInt(4, Integer.parseInt(txtEdad.getText()));
                            ps.setString(5, txtSexo.getText());
                            ps.setString(6, txtDirecion.getText());
                            ps.setDate(7, new java.sql.Date(jpcFechaNac.getDate().getTime()));
                            ps.setString(8, txtMail.getText());
                            ps.setDouble(9, Double.parseDouble(txtEstatura.getText()));
                            ps.setDouble(10, Double.parseDouble(txtPeso.getText()));
                            ps.setString(11, txtOcupacion.getText());
                            ps.setInt(12, Integer.parseInt(txtIdEnfermero.getText()));
                            ps.execute();
                            ps.close();
                        } catch (IOException ex) {
                            Logger.getLogger(frmModificarPaciente.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        JOptionPane.showMessageDialog(this, "Datos guardados correctamente...",
                            "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
                    }catch(SQLException | NumberFormatException | HeadlessException x){
                        JOptionPane.showMessageDialog(rootPane, "Debe ingresar datos "+x);
                    }finally {
                        con.desconectar();
                    }
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

