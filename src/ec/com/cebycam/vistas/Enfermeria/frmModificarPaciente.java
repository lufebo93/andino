    public void cargarTabla() throws IOException{
        Conexion con = new Conexion();
        con.conectar();
        
        modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Paciente");
        modelo.addColumn("Cedula");
        modelo.addColumn("Telefono");
        modelo.addColumn("Edad");
        modelo.addColumn("Sexo");
        modelo.addColumn("Dirección");
        modelo.addColumn("Fecha de Nacimiento");
        modelo.addColumn("Mail");
        modelo.addColumn("Estatura");
        modelo.addColumn("Peso");
        modelo.addColumn("Ocupación");
        modelo.addColumn("Foto");
        modelo.addColumn("Categoria");
        
        java.util.List<HistoriaClinica> lst = new ArrayList<>();
        HistoriaClinicaImp dao = new HistoriaClinicaImp();
        
        try {            
            lst = dao.obtener();            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al procesar los datos "+e,
                        "CEBYCAM-CES", JOptionPane.INFORMATION_MESSAGE);
        }finally {
            con.desconectar();
        }
        
        for(HistoriaClinica exp:lst){
           modelo.addRow(new Object[]{
               exp.getCodigo(), 
               exp.getPaciente(),
               exp.getCedula(),
               exp.getTelefono(),
               exp.getEdad(),
               exp.getSexo(),
               exp.getDireccion(),
               exp.getFecha_nacimiento(),
               exp.getMail(),
               exp.getEstatura(),
               exp.getPeso(),
               exp.getOcupacion(),
               exp.getImagen(),
               exp.getCategoria(),
           });        
        }
        tabla.setModel(modelo);
        modelo.fireTableDataChanged();
        
        tabla.getColumnModel().getColumn(12).setMaxWidth(0);
        tabla.getColumnModel().getColumn(12).setMinWidth(0);
        tabla.getColumnModel().getColumn(12).setPreferredWidth(0);
        
        tabla.getColumnModel().getColumn(13).setMaxWidth(0);
        tabla.getColumnModel().getColumn(13).setMinWidth(0);
        tabla.getColumnModel().getColumn(13).setPreferredWidth(0);
    }
    
    public void ordenarTabla(){
        TableRowSorter<TableModel> ordenar = new TableRowSorter<>(modelo);
        tabla.setRowSorter(ordenar);
    }
    
    public void filtro() {
        int columnaABuscar = 0;
        if (cmbListar.getSelectedItem().equals("Código")) {
            columnaABuscar = 0;
        }
        if (cmbListar.getSelectedItem() == "Paciente") {
            columnaABuscar = 1;
        }
        if (cmbListar.getSelectedItem() == "Cédula") {
            columnaABuscar = 2;
        }
        String replace = txtBusqueda.getText();
        replace = replace.replace("e", "(é)");
        trsFiltro.setRowFilter(RowFilter.regexFilter("(?i).*"+replace+".*", columnaABuscar));
        tabla.setRowSorter(trsFiltro);
    }
    
