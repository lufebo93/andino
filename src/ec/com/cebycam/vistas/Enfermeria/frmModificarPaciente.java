    private void createAndShowGUI(){
        img=new JLabel();
        img.setPreferredSize(new Dimension(230,300));
        jf.setAccessory(img);
        jf.setAcceptAllFileFilterUsed(false);
        jf.addPropertyChangeListener((final PropertyChangeEvent pe) -> {
            SwingWorker<Image,Void> worker=new SwingWorker<Image,Void>(){
                @Override
                protected Image doInBackground(){
                    if(pe.getPropertyName().equals(JFileChooser.SELECTED_FILE_CHANGED_PROPERTY)){
                        File f=jf.getSelectedFile();
                        try{
                            FileInputStream fin=new FileInputStream(f);
                            BufferedImage bim=ImageIO.read(fin);
                            return bim.getScaledInstance(230,300,BufferedImage.SCALE_SMOOTH);
                        }catch(IOException e){
                            JOptionPane.showMessageDialog(null, "Error: "+ e);
                        }
                    }
                    return null;
                }
                @Override
                protected void done(){
                    try{
                        Image i=get(1L,TimeUnit.NANOSECONDS);
                        if(i==null) return;
                        img.setIcon(new ImageIcon(i));
                    }catch(InterruptedException | ExecutionException | TimeoutException e){
                        //img.setText("Se presentó un error "+e);
                    }
                }
            };
            worker.execute();
        });
    }
