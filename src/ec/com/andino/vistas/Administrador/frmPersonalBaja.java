    public void cargarTabla() throws SQLException, IOException{
        Conexion con = new Conexion();
        con.conectar();
        ResultSet rs = null;
        
        modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Especialidad");
        modelo.addColumn("Direccion");
        modelo.addColumn("Telefono");
        modelo.addColumn("Cedula");
        modelo.addColumn("Sexo");
        modelo.addColumn("Fecha_Inicio");
        modelo.addColumn("Fecha_Fin");
        modelo.addColumn("Administrador");
        
        try{
            rs = con.ejecutarQuery("SELECT \n" +
"\"personal\".codigo, \n" +
"\"personal\".nombre, \n" +
"\"personal\".especialidad, \n" +
"\"personal\".direccion, \n" +
"\"personal\".telefono, \n" +
"\"personal\".cedula, \n" +
"\"personal\".sexo, \n" +
"\"personal\".fecha_inicio, \n" +
"\"personal\".fecha_fin, \n" +
"\"administrador\".nombre\n" +
"  FROM \"personal_baja\" personal inner join \"administrador\" administrador on \"personal\".administrador=administrador.codigo");
                ResultSetMetaData md = rs.getMetaData();
                int columnCount = md.getColumnCount();
                String[] cols = new String[columnCount];
                for (int i=1;i<= columnCount;i++){
                    cols[i-1] = md.getColumnName(i);
                }
                while (rs.next()){
                    Object[] row = new Object[columnCount];
                    for (int i = 1 ; i <= columnCount ; i++){
                        row[i-1] = rs.getObject(i);
                    }
                    modelo.addRow(row);
                }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Error "+e,
                    "HE.ANDINO", JOptionPane.INFORMATION_MESSAGE);
        }finally {
            con.desconectar();
            rs.close();
        }
        tablaPersonalBaja.setModel(modelo);
        modelo.fireTableDataChanged();
    }
    
